<?php

//simple if statement
$a = 50;
$b = 10;
if ($a > $b)  {
  echo "Hello World";
}

//simple if NOTstatement
$a = 50;
$b = 10;
if ($a != $b)  {
  echo "Hello World";
}

//Simple if..else.. statement
$a = 50;
$b = 10;
if ($a == $b) {
  echo "Yes";
} else {
  echo "No";
}

//Longer if..elseif..else.. statement
$a = 50;
$b = 10;
if ($a == $b) {
  echo "1";
} elseif ($a > $b) {
  echo "2";
} else {
  echo "3";
}


